24 != 5 = @{[   24 != 5   ]}
24<=> 5 = @{[   24<=> 5   ]}
24<=>24 = @{[   24<=>24   ]}
 5<=>24 = @{[    5<=>24   ]}

論理演算子
1 and 0 = @{[   1 and 0   ]}
