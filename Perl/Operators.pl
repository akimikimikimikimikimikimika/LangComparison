  not 0 = @{[     not 0   ]}
    ! 0 = @{[       ! 0   ]}

ビット演算子 (2進数での各桁毎の論理演算)
1010 & 1001 = @{[sprintf("%04b",   oct("0b1010") & oct("0b1001")   )]} (AND)
1010 | 1001 = @{[sprintf("%04b",   oct("0b1010") | oct("0b1001")   )]} (OR)
1010 ^ 1001 = @{[sprintf("%04b",   oct("0b1010") ^ oct("0b1001")   )]} (XOR)
     ~ 1001 = @{[sprintf("%04b",                 ~ oct("0b1001")   %16)]} (NOT)

111111 << 2 = @{[sprintf("%b",   oct("0b111111") << 2   )]} (2桁左シフト)
111111 >> 2 = @{[sprintf("%b",   oct("0b111111") >> 2   )]} (2桁右シフト)

三項演算子
1 ? "yes" : "no" = "@{[   1 ? "yes" : "no"   ]}"
0 ? "yes" : "no" = "@{[   0 ? "yes" : "no"   ]}"

文字列演算子
"abc" . "def" = "@{[   "abc" . "def"   ]}"
"abc" x 3     = "@{[   "abc" x 3       ]}"

文字列の別の表現
qq(a"b"c) = @{[   qq(a"b"c)   ]} (エスケープ不要)
q(that's) = @{[   q(that's)   ]} (エスケープ不要)

文字列比較演算子
"abc"  eq "def" = @{[   "abc"  eq "def"   ]} (等しい)
"abc"  ne "def" = @{[   "abc"  ne "def"   ]} (等しくない)
"abc"  lt "def" = @{[   "abc"  lt "def"   ]} (辞書順で "abc" < "def")
"abc"  le "def" = @{[   "abc"  le "def"   ]} (辞書順で "abc" ≦ "def")
"abc"  gt "def" = @{[   "abc"  gt "def"   ]} (辞書順で "abc" > "def")
"abc"  ge "def" = @{[   "abc"  ge "def"   ]} (辞書順で "abc" ≧ "def")
"abc" cmp "def" = @{[   "abc" cmp "def"   ]} (辞書順で "abc"<=>"def" を演算)

配列演算子
(1,2,3) x 3        = @{[   (1,2,3) x 3          ]}
(1,2,3) == (1,2,3) = @{[   (1,2,3) == (1,2,3)   ]}
(1,2) <=> (1,2,3)  = @{[   (1,2) <=> (1,2,3)    ]}

正規表現演算子
